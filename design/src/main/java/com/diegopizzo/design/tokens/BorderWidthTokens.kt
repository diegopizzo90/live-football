package com.diegopizzo.design.tokens

import androidx.compose.ui.unit.dp

/**
 * The border width tokens are as follows:
 * - Hairline: 1dp
 * - Thin: 2dp
 * - Thick: 4dp
 */
object BorderWidthTokens {
    /**
     * Hairline border width (1dp)
     */
    val Hairline = 1.dp

    /**
     * Thin border width (2dp)
     */
    val Thin = 2.dp

    /**
     * Thick border width (4dp)
     */
    val Thick = 4.dp
}