package com.diegopizzo.design.tokens

import androidx.compose.ui.unit.dp

/**
 * The spacing tokens are as follows:
 * - None: 0dp
 * - Micro: 2dp
 * - Mini: 4dp
 * - Small: 8dp
 * - Medium: 12dp
 * - Large: 16dp
 * - ExtraLarge: 24dp
 * - Big: 32dp
 * - Huge: 48dp
 * - Super: 64dp
 * - Massive: 96dp
 */
object SpaceTokens {
    /**
     * No space (0dp)
     */
    val None = 0.dp

    /**
     * Micro space (2dp)
     */
    val Micro = 2.dp

    /**
     * Mini space (4dp)
     */
    val Mini = 4.dp

    /**
     * Small space (8dp)
     */
    val Small = 8.dp

    /**
     * Medium space (12dp)
     */
    val Medium = 12.dp

    /**
     * Large space (16dp)
     */
    val Large = 16.dp

    /**
     * Medium large space (20dp)
     */
    val MediumLarge = 20.dp

    /**
     * Extra large space (24dp)
     */
    val ExtraLarge = 24.dp

    /**
     * Big space (32dp)
     */
    val Big = 32.dp

    /**
     * Huge space (48dp)
     */
    val Huge = 48.dp

    /**
     * Super space (64dp)
     */
    val Super = 64.dp

    /**
     * Massive space (96dp)
     */
    val Massive = 96.dp
}
